// ASSIGNMENT OPERATORS!!

#include <iostream>

int main() {
    int x = 10; // You don't read this as x is equals to 10, but rather 10 assigned to the value of x.
    x += 5; // The addition assignment. This is the same as x = x + 5, but shorthanded.
    x -= 2; // Same as addition assignment, but this time we substract by 2.
}

